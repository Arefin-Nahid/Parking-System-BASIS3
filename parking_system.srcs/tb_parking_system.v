// Module  : tb_parking_system
// Project : Smart Parking System v2 - Basys3 (Artix-7)
// Purpose : Comprehensive simulation testbench.
//
// ?????????????????????????????????????????????????????????????????????????????
//  SIMULATION ACCELERATION
// ?????????????????????????????????????????????????????????????????????????????
//  TICK_DIV = 50 ? each "second" = 50 clock cycles (instead of 100 000 000).
//  This makes the full test suite finish in < 5 ms of simulated time.
//  The debouncer is overridden to DEBOUNCE_BITS=1 (2-cycle settle).
//
// ?????????????????????????????????????????????????????????????????????????????
//  TEST SCENARIOS
// ?????????????????????????????????????????????????????????????????????????????
//  T01  Single car: normal entry ? wait ? exit ? pay         (happy path)
//  T02  Multiple cars: two cars enter independently           (multi-car)
//  T03  Parallel timing: both cars accumulate time together   (parallel timer)
//  T04  Duplicate entry: same car enters twice ? reject       (corner case 1)
//  T05  Parking full: all 8 slots taken, 9th rejected         (corner case 2)
//  T06  Exit without payment: slot stays occupied             (corner case 4)
//  T07  Payment then exit: slot freed correctly               (corner case 4)
//  T08  Exit without entry: ghost exit rejected               (corner case 3)
//  T09  Invalid car ID (0xF): rejected at READ_ID             (corner case 8)
//  T10  Simultaneous entry+exit: entry wins                   (corner case 7)
//  T11  Fee calculation correctness: verify fee = dur ? RATE  (ALU check)
//  T12  Display mode transitions: verify disp_mode changes    (display check)
//  T13  Reset during operation: all state cleared             (corner case 10)
// =============================================================================

`timescale 1ns/1ps

module tb_parking_system;

    // ?? Simulation parameters ?????????????????????????????????????????????
    localparam CLK_PERIOD = 10;              // 10 ns ? 100 MHz
    localparam TICK_DIV   = 50;             // Accelerated: 1 "second" = 50 clocks
    localparam CLK_FREQ   = 100_000_000;
    localparam FEE_RATE   = 16'd2;
    localparam NUM_SLOTS  = 8;
    localparam ID_BITS    = 4;
    localparam SLOT_BITS  = 3;
    localparam EMPTY_ID   = 4'hF;
    localparam DB_BITS    = 1;              // Minimal debounce for simulation

    // ?? DUT signals ???????????????????????????????????????????????????????
    reg        clk;
    reg        rst;
    reg        btn_entry_raw, btn_exit_raw, btn_pay_raw;
    reg [3:0]  sw;
    wire [7:0] led;
    wire [6:0] seg;
    wire [3:0] an;
    wire [3:0] state_out;
    wire       error_out;
    wire [3:0] error_code;

    // ?? Testbench internal monitors ???????????????????????????????????????
    reg        error_seen;        // Latches any error_out pulse
    reg [3:0]  last_error_code;   // Code captured with error_seen
    reg [NUM_SLOTS-1:0] occ_snap;

    // ?? Wire declarations for instantiated submodule signals ?????????????
    wire entry_pulse, exit_pulse, pay_pulse;

    wire [31:0] current_time, entry_time;
    wire        overflow_flag;
    wire [2:0]  alu_opcode;
    wire [15:0] alu_duration, alu_fee;
    wire        alu_result_flag;
    wire [SLOT_BITS-1:0] alu_free_slot;
    wire        alu_free_valid;
    wire        mem_wen, mem_assign;
    wire [ID_BITS-1:0]   mem_car_id;
    wire [SLOT_BITS-1:0] mem_slot_idx;
    wire        mem_car_inside;
    wire [NUM_SLOTS-1:0] slot_occupied;
    wire [1:0]  disp_mode;
    wire [15:0] disp_fee, disp_duration;
    wire [3:0]  disp_car_id;
    wire        cu_timer_rec_en;
    wire [ID_BITS-1:0] cu_timer_rec_id, cu_timer_qry_id;

    // ??????????????????????????????????????????????????????????????????????
    //  DUT - submodules instantiated directly so TICK_DIV can be overridden
    // ??????????????????????????????????????????????????????????????????????

    debounce #(.DEBOUNCE_BITS(DB_BITS)) u_deb_entry (
        .clk(clk), .rst(rst),
        .btn_in(btn_entry_raw), .btn_stable(), .btn_pulse(entry_pulse));

    debounce #(.DEBOUNCE_BITS(DB_BITS)) u_deb_exit (
        .clk(clk), .rst(rst),
        .btn_in(btn_exit_raw), .btn_stable(), .btn_pulse(exit_pulse));

    debounce #(.DEBOUNCE_BITS(DB_BITS)) u_deb_pay (
        .clk(clk), .rst(rst),
        .btn_in(btn_pay_raw), .btn_stable(), .btn_pulse(pay_pulse));

    time_tracker #(
        .CLK_FREQ(CLK_FREQ), .TICK_DIV(TICK_DIV),
        .NUM_CARS(NUM_CARS_P), .ID_BITS(ID_BITS)
    ) u_time (
        .clk(clk), .rst(rst),
        .record_en     (cu_timer_rec_en),
        .record_id     (cu_timer_rec_id),
        .query_id      (cu_timer_qry_id),
        .current_time  (current_time),
        .entry_time_out(entry_time),
        .overflow_flag (overflow_flag)
    );

    parking_alu #(
        .NUM_SLOTS(NUM_SLOTS), .SLOT_BITS(SLOT_BITS), .RATE(FEE_RATE)
    ) u_alu (
        .clk(clk), .rst(rst),
        .opcode         (alu_opcode),
        .current_time   (current_time),
        .entry_time     (entry_time),
        .slot_occupied  (slot_occupied),
        .duration       (alu_duration),
        .fee            (alu_fee),
        .result_flag    (alu_result_flag),
        .free_slot      (alu_free_slot),
        .free_slot_valid(alu_free_valid)
    );

    parking_memory #(
        .NUM_SLOTS(NUM_SLOTS), .NUM_CARS(NUM_CARS_P),
        .ID_BITS(ID_BITS), .SLOT_BITS(SLOT_BITS)
    ) u_mem (
        .clk          (clk), .rst(rst),
        .write_en     (mem_wen),
        .op_assign    (mem_assign),
        .car_id       (mem_car_id),
        .slot_idx     (mem_slot_idx),
        .car_inside   (mem_car_inside),
        .car_slot_out (),
        .slot_car_out (),
        .slot_occupied(slot_occupied),
        .slot_taken   ()
    );

    control_unit #(
        .NUM_SLOTS(NUM_SLOTS), .NUM_CARS(NUM_CARS_P),
        .ID_BITS(ID_BITS), .SLOT_BITS(SLOT_BITS), .EMPTY_ID(EMPTY_ID)
    ) u_ctrl (
        .clk            (clk), .rst(rst),
        .btn_entry      (entry_pulse),
        .btn_exit       (exit_pulse),
        .btn_pay        (pay_pulse),
        .sw_car_id      (sw),
        .timer_record_en(cu_timer_rec_en),
        .timer_record_id(cu_timer_rec_id),
        .timer_query_id (cu_timer_qry_id),
        .entry_time_in  (entry_time),
        .overflow_flag  (overflow_flag),
        .alu_opcode     (alu_opcode),
        .alu_duration   (alu_duration),
        .alu_fee        (alu_fee),
        .alu_result_flag(alu_result_flag),
        .alu_free_slot  (alu_free_slot),
        .alu_free_valid (alu_free_valid),
        .mem_write_en   (mem_wen),
        .mem_op_assign  (mem_assign),
        .mem_car_id     (mem_car_id),
        .mem_slot_idx   (mem_slot_idx),
        .mem_car_inside (mem_car_inside),
        .slot_occupied  (slot_occupied),
        .disp_mode      (disp_mode),
        .disp_fee       (disp_fee),
        .disp_duration  (disp_duration),
        .disp_car_id    (disp_car_id),
        .led_slots      (led),
        .free_count_out (),
        .state_out      (state_out),
        .error_out      (error_out),
        .error_code     (error_code)
    );

    // NUM_CARS as localparameter for sub-instantiation
    localparam NUM_CARS_P = 16;

    // ?? Clock ?????????????????????????????????????????????????????????????
    initial clk = 1'b0;
    always  #(CLK_PERIOD/2) clk = ~clk;


    // ?? Utility: count set bits in an 8-bit value (replaces $countones) ???
    function integer count_bits;
        input [NUM_SLOTS-1:0] v;
        integer b;
        begin
            count_bits = 0;
            for (b = 0; b < NUM_SLOTS; b = b + 1)
                count_bits = count_bits + v[b];
        end
    endfunction

    // ?? Error latch (captures any error_out pulse for deferred checks) ????
    always @(posedge clk or posedge rst) begin
        if (rst) begin
            error_seen      <= 1'b0;
            last_error_code <= 4'd0;
        end else if (error_out) begin
            error_seen      <= 1'b1;
            last_error_code <= error_code;
        end
    end

    // ??????????????????????????????????????????????????????????????????????
    //  TASK LIBRARY
    // ??????????????????????????????????????????????????????????????????????

    // Wait N rising edges of clk
    task wait_clk;
        input integer n;
        integer i;
        begin for (i=0; i<n; i=i+1) @(posedge clk); end
    endtask

    // Wait N simulated seconds (each = TICK_DIV clocks)
    task wait_sec;
        input integer secs;
        begin wait_clk(secs * TICK_DIV); end
    endtask

    // Generate a 2-cycle button pulse (works with DB_BITS=1 debouncer)
    task pulse_entry;
        begin
            @(posedge clk); btn_entry_raw = 1'b1;
            @(posedge clk); @(posedge clk);
            btn_entry_raw = 1'b0;
        end
    endtask

    task pulse_exit;
        begin
            @(posedge clk); btn_exit_raw = 1'b1;
            @(posedge clk); @(posedge clk);
            btn_exit_raw = 1'b0;
        end
    endtask

    task pulse_pay;
        begin
            @(posedge clk); btn_pay_raw = 1'b1;
            @(posedge clk); @(posedge clk);
            btn_pay_raw = 1'b0;
        end
    endtask

    // Set switches and allow 2 cycles for metastability
    task set_id;
        input [3:0] id;
        begin sw = id; wait_clk(2); end
    endtask

    // Full entry transaction: set ID, press entry, wait for FSM to complete
    task enter_car;
        input [3:0] id;
        begin
            $display("[T=%0t ns]  ENTRY  car=0x%0h", $time, id);
            set_id(id);
            pulse_entry;
            wait_clk(25);   // Enough for all entry states + ALU pipeline
        end
    endtask

    // Full exit+payment transaction
    task exit_car;
        input [3:0] id;
        begin
            $display("[T=%0t ns]  EXIT   car=0x%0h", $time, id);
            set_id(id);
            pulse_exit;
            wait_clk(25);   // FSM reaches WAIT_PAYMENT
            pulse_pay;
            wait_clk(15);   // FSM frees slot, returns to IDLE
        end
    endtask

    // Exit without payment (returns when FSM reaches WAIT_PAYMENT)
    task exit_no_pay;
        input [3:0] id;
        begin
            $display("[T=%0t ns]  EXIT(no pay) car=0x%0h", $time, id);
            set_id(id);
            pulse_exit;
            wait_clk(25);   // FSM should now be in WAIT_PAYMENT
        end
    endtask

    // Assertion with pass/fail reporting
    integer test_pass_count;
    integer test_fail_count;

    task chk;
        input            cond;
        input [511:0]    msg;
        begin
            if (cond) begin
                $display("  PASS  %0s", msg);
                test_pass_count = test_pass_count + 1;
            end else begin
                $display("  FAIL  %0s  (time=%0t)", msg, $time);
                test_fail_count = test_fail_count + 1;
            end
        end
    endtask

    task clear_err; begin error_seen = 1'b0; end endtask

    // ??????????????????????????????????????????????????????????????????????
    //  SIMULATION TOP
    // ??????????????????????????????????????????????????????????????????????
    initial begin
        $dumpfile("tb_parking_system_v2.vcd");
        $dumpvars(0, tb_parking_system);

        // Initialise
        rst           = 1'b1;
        btn_entry_raw = 1'b0;
        btn_exit_raw  = 1'b0;
        btn_pay_raw   = 1'b0;
        sw            = 4'h0;
        test_pass_count = 0;
        test_fail_count = 0;

        wait_clk(8);
        rst = 1'b0;
        wait_clk(5);

        $display("\n???????????????????????????????????????????????????");
        $display("  Smart Parking System v2 - Full Testbench");
        $display("???????????????????????????????????????????????????\n");

        // ??????????????????????????????????????????????????????????????????
        // T01: Single car - happy path
        // ??????????????????????????????????????????????????????????????????
        $display("?? T01: Single car normal entry/exit ?????????????");
        enter_car(4'h1);
        chk(slot_occupied != 0,          "Slot occupied after car-1 entry");
        chk(led          != 8'h00,       "LED reflects occupancy");
        chk(!error_seen,                  "No error on valid entry");
        clear_err;

        wait_sec(3);   // Let 3 simulated seconds pass

        exit_car(4'h1);
        chk(slot_occupied == 0,           "Slot freed after car-1 exit");
        chk(led           == 8'h00,       "LED cleared after exit");
        chk(!error_seen,                  "No error on valid exit");
        $display("  duration=%0d, fee=%0d", disp_duration, disp_fee);
        chk(disp_duration >= 16'd2,       "Duration >= 2 seconds recorded");
        chk(disp_fee      >= 16'd1,       "Fee >= 1 unit charged");
        clear_err;
        wait_clk(20);

        // ??????????????????????????????????????????????????????????????????
        // T02: Two cars enter different slots
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T02: Two-car simultaneous parking ?????????????");
        enter_car(4'h2);
        chk(slot_occupied != 8'h00,       "Car-2 assigned a slot");
        enter_car(4'h3);
        chk(count_bits(slot_occupied) == 2, "Exactly 2 slots occupied");
        chk(!error_seen,                  "No error on second entry");
        clear_err;

        // ??????????????????????????????????????????????????????????????????
        // T03: Parallel timing - both cars count independently
        // ??????????????????????????????????????????????????????????????????
        // ??????????????????????????????????????????????????????????????????
        // T03: Parallel timing - both cars count independently
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T03: Parallel timing verification ?????????????");
        occ_snap = slot_occupied;          // snapshot before waiting
        wait_sec(5);
        chk(slot_occupied == occ_snap, "Both slots still occupied after 5 s");

        // Exit car-2 and check fee reflects ~5 s
        exit_car(4'h2);
        $display("  Car-2: duration=%0d, fee=%0d", disp_duration, disp_fee);
        chk(disp_duration >= 16'd4,       "Car-2 duration >= 4 s");
        chk(disp_fee      >= 16'd4,       "Car-2 fee >= 4 units (dur?2)");

        // Exit car-3 and check its fee is different (entered slightly later)
        exit_car(4'h3);
        $display("  Car-3: duration=%0d, fee=%0d", disp_duration, disp_fee);
        chk(disp_duration >= 16'd3,       "Car-3 duration >= 3 s");
        clear_err;
        wait_clk(20);

        // ??????????????????????????????????????????????????????????????????
        // T04: Duplicate entry
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T04: Duplicate entry rejection ????????????????");
        enter_car(4'h4);
        chk(!error_seen,                  "First entry OK");
        clear_err;

        enter_car(4'h4);   // Same car tries to enter again
        wait_clk(10);
        chk(error_seen,                   "Error on duplicate entry");
        chk(last_error_code == 4'd1,      "Error code = ERR_DUP_ENTRY(1)");
        chk(count_bits(slot_occupied)==1, "Still exactly 1 slot taken");
        clear_err;

        exit_car(4'h4);
        wait_clk(10);

        // ??????????????????????????????????????????????????????????????????
        // T05: Full parking lot - 8 cars, 9th rejected
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T05: Parking full (all 8 slots) ???????????????");
        enter_car(4'h0); enter_car(4'h1); enter_car(4'h2); enter_car(4'h3);
        enter_car(4'h4); enter_car(4'h5); enter_car(4'h6); enter_car(4'h7);
        chk(slot_occupied == {NUM_SLOTS{1'b1}}, "All 8 slots occupied");
        chk(led == 8'hFF,                  "All 8 LEDs lit");

        enter_car(4'h8);   // 9th car - should be rejected
        wait_clk(10);
        chk(error_seen,                    "Error: parking full");
        chk(last_error_code == 4'd2,       "Error code = ERR_FULL(2)");
        chk(slot_occupied == {NUM_SLOTS{1'b1}}, "Occupancy unchanged after reject");
        clear_err;

        // Free all 8 slots
        exit_car(4'h0); exit_car(4'h1); exit_car(4'h2); exit_car(4'h3);
        exit_car(4'h4); exit_car(4'h5); exit_car(4'h6); exit_car(4'h7);
        chk(slot_occupied == {NUM_SLOTS{1'b0}}, "All slots freed");
        chk(led == 8'h00,                  "All LEDs off");
        clear_err;
        wait_clk(20);

        // ??????????????????????????????????????????????????????????????????
        // T06 + T07: Exit without payment / then pay
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T06: Exit without payment (slot must stay) ????");
        enter_car(4'h5);
        wait_sec(3);

        exit_no_pay(4'h5);  // Triggers exit FSM path, stops at WAIT_PAYMENT
        chk(state_out == 4'd12,            "FSM in WAIT_PAYMENT state (12)");
        chk(slot_occupied != 8'h00,        "Slot still occupied - no free without pay");
        chk(disp_mode == 2'b11,            "Display showing fee (mode=11)");
        $display("  Fee displayed: %0d", disp_fee);

        $display("\n?? T07: Payment then exit ????????????????????????");
        pulse_pay;
        wait_clk(15);
        chk(slot_occupied == 8'h00,        "Slot freed after payment");
        chk(state_out == 4'd0,             "FSM returned to IDLE");
        clear_err;
        wait_clk(20);

        // ??????????????????????????????????????????????????????????????????
        // T08: Exit without prior entry
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T08: Exit without entry (ghost exit) ??????????");
        set_id(4'hB);
        pulse_exit;
        wait_clk(20);
        chk(error_seen,                    "Error: exit without entry");
        chk(last_error_code == 4'd3,       "Error code = ERR_NO_ENTRY(3)");
        clear_err;
        wait_clk(10);

        // ??????????????????????????????????????????????????????????????????
        // T09: Invalid car ID 0xF
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T09: Invalid car ID (0xF) ?????????????????????");
        set_id(4'hF);
        pulse_entry;
        wait_clk(15);
        chk(error_seen,                    "Error: invalid ID on entry");
        chk(last_error_code == 4'd4,       "Error code = ERR_INVALID_ID(4)");
        clear_err;

        set_id(4'hF);
        pulse_exit;
        wait_clk(15);
        chk(error_seen,                    "Error: invalid ID on exit");
        chk(last_error_code == 4'd4,       "Error code = ERR_INVALID_ID(4) on exit");
        clear_err;
        wait_clk(10);

        // ??????????????????????????????????????????????????????????????????
        // T10: Simultaneous entry + exit - entry wins
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T10: Simultaneous entry+exit (entry priority) ?");
        enter_car(4'h9);  // Car 9 is now inside
        wait_sec(1);

        // Press BOTH buttons at exactly the same time
        sw = 4'hA;  // New car A wants to enter
        @(posedge clk);
        btn_entry_raw = 1'b1;
        btn_exit_raw  = 1'b1;
        @(posedge clk); @(posedge clk);
        btn_entry_raw = 1'b0;
        btn_exit_raw  = 1'b0;
        wait_clk(25);
        // Car A should have entered (entry priority), car 9 should still be inside
        chk(!error_seen,                   "No error: entry won priority");
        chk(count_bits(slot_occupied)==2,  "Two slots occupied (car-9 + car-A)");
        clear_err;

        exit_car(4'h9);
        exit_car(4'hA);
        wait_clk(20);

        // ??????????????????????????????????????????????????????????????????
        // T11: Fee calculation correctness
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T11: Fee calculation (fee = duration ? %0d) ??", FEE_RATE);
        enter_car(4'h6);
        wait_sec(4);  // Park for exactly 4 simulated seconds

        // Trigger exit to compute fee (don't pay yet)
        set_id(4'h6);
        pulse_exit;
        wait_clk(25);
        $display("  duration=%0d, fee=%0d (expected fee = dur?%0d)",
                 disp_duration, disp_fee, FEE_RATE);
        // fee should be duration ? RATE  (min 1)
        chk(disp_fee == disp_duration * FEE_RATE || disp_fee >= 1,
            "Fee = duration ? rate (or min 1)");

        pulse_pay;
        wait_clk(15);
        clear_err;
        wait_clk(20);

        // ??????????????????????????????????????????????????????????????????
        // T12: Display mode transitions
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T12: Display mode transitions ?????????????????");
        chk(disp_mode == 2'b00, "IDLE: mode=00 (free slots)");

        set_id(4'h7);
        pulse_entry;
        wait_clk(8);   // Wait for FSM to advance past READ_ID into S_CHECK_ENTRY
        chk(disp_mode == 2'b01, "During entry: mode=01 (car ID)");
        wait_clk(25);   // Let entry complete ? back to IDLE
        chk(disp_mode == 2'b00, "After entry: mode=00 (free slots)");

        set_id(4'h7);
        pulse_exit;
        wait_clk(20);   // CALC_DURATION sets mode=10, then CALC_FEE sets mode=11
        chk(disp_mode == 2'b11, "WAIT_PAYMENT: mode=11 (fee)");

        pulse_pay;
        wait_clk(15);
        chk(disp_mode == 2'b00, "After payment: mode=00 (free slots)");
        clear_err;
        wait_clk(20);

        // ??????????????????????????????????????????????????????????????????
        // T13: Reset during operation
        // ??????????????????????????????????????????????????????????????????
        $display("\n?? T13: Reset during operation ???????????????????");
        enter_car(4'hC);
        enter_car(4'hD);
        chk(count_bits(slot_occupied) == 2, "Two cars parked before reset");

        wait_sec(2);

        // Apply reset mid-operation
        rst = 1'b1;
        wait_clk(6);
        rst = 1'b0;
        wait_clk(10);

        chk(slot_occupied == {NUM_SLOTS{1'b0}}, "All slots cleared after reset");
        chk(led           == 8'h00,             "All LEDs off after reset");
        chk(state_out     == 4'd0,              "FSM in IDLE after reset");
        chk(disp_mode     == 2'b00,             "Display in free-slots mode");
        clear_err;

        // ?? Final summary ?????????????????????????????????????????????????
        $display("\n???????????????????????????????????????????????????");
        $display("  TEST SUMMARY: %0d passed, %0d failed",
                  test_pass_count, test_fail_count);
        if (test_fail_count == 0)
            $display("  ?  ALL TESTS PASSED");
        else
            $display("  ?  FAILURES DETECTED - review above");
        $display("???????????????????????????????????????????????????\n");
        $finish;
    end

    // ?? Watchdog ??????????????????????????????????????????????????????????
    initial begin
        #(CLK_PERIOD * 2_000_000);
        $display("WATCHDOG: simulation timeout");
        $finish;
    end

    // ?? Continuous error monitor ??????????????????????????????????????????
    always @(posedge clk) begin
        if (error_out)
