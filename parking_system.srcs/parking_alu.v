// Module  : parking_alu (TIMING-OPTIMIZED v3)
// Fixes:
//   ? Multiplier pipelined (critical path removed)
//   ? Clean 2-stage pipeline design
//   ? Compatible with your existing FSM (alu_ready still works)
// =============================================================================

module parking_alu #(
    parameter NUM_SLOTS  = 8,
    parameter SLOT_BITS  = 3,
    parameter [15:0] RATE = 16'd2
)(
    input  wire        clk,
    input  wire        rst,
    input  wire [2:0]  opcode,
    input  wire [31:0] current_time,
    input  wire [31:0] entry_time,
    input  wire [NUM_SLOTS-1:0] slot_occupied,

    output reg  [15:0] duration,
    output reg  [15:0] fee,
    output reg         result_flag,
    output reg  [SLOT_BITS-1:0] free_slot,
    output reg         free_slot_valid
);

    // ?? Opcode ???????????????????????????????????????????????????????????
    localparam [2:0]
        OP_NOP        = 3'b000,
        OP_CALC_DUR   = 3'b001,
        OP_CALC_FEE   = 3'b010,
        OP_CHECK_FULL = 3'b011,
        OP_CHECK_EMPTY= 3'b100,
        OP_FIND_FREE  = 3'b101;

    // ?????????????????????????????????????????????????????????????????????
    // STAGE 0: Pre-compute slot logic (already good)
    // ?????????????????????????????????????????????????????????????????????

    reg [SLOT_BITS:0]   occ_count_r;
    reg [SLOT_BITS-1:0] free_idx_r;
    reg                 found_r;

    integer k, j;
    reg [SLOT_BITS:0]   occ_count_c;
    reg [SLOT_BITS-1:0] free_idx_c;
    reg                 found_c;

    always @(*) begin
        occ_count_c = 0;
        for (k = 0; k < NUM_SLOTS; k = k + 1)
            occ_count_c = occ_count_c + slot_occupied[k];
    end

    always @(*) begin
        free_idx_c = 0;
        found_c    = 0;
        for (j = NUM_SLOTS-1; j >= 0; j = j - 1) begin
            if (!slot_occupied[j]) begin
                free_idx_c = j[SLOT_BITS-1:0];
                found_c    = 1'b1;
            end
        end
    end

    always @(posedge clk or posedge rst) begin
        if (rst) begin
            occ_count_r <= 0;
            free_idx_r  <= 0;
            found_r     <= 0;
        end else begin
            occ_count_r <= occ_count_c;
            free_idx_r  <= free_idx_c;
            found_r     <= found_c;
        end
    end

    // ?????????????????????????????????????????????????????????????????????
    // STAGE 1: Duration calculation (REGISTERED)
    // ?????????????????????????????????????????????????????????????????????

    wire [31:0] raw_dur = (current_time >= entry_time)
                        ? (current_time - entry_time)
                        : 32'd0;

    reg [15:0] duration_stage;

    always @(posedge clk) begin
        duration_stage <= (raw_dur > 32'd65535) ? 16'hFFFF : raw_dur[15:0];
    end

    // ?????????????????????????????????????????????????????????????????????
    // STAGE 2: MULTIPLIER PIPELINE (CRITICAL FIX)
    // ?????????????????????????????????????????????????????????????????????

    (* use_dsp = "yes" *) reg [31:0] fee_mult_stage;

    always @(posedge clk) begin
        fee_mult_stage <= duration_stage * RATE;
    end

    // ?????????????????????????????????????????????????????????????????????
    // STAGE 3: FINAL OUTPUT LOGIC
    // ?????????????????????????????????????????????????????????????????????

    always @(posedge clk or posedge rst) begin
        if (rst) begin
            duration        <= 0;
            fee             <= 0;
            result_flag     <= 0;
            free_slot       <= 0;
            free_slot_valid <= 0;
        end else begin
            result_flag     <= 0;
            free_slot_valid <= 0;

            case (opcode)

                // ?? Duration ????????????????????????????????????????????
                OP_CALC_DUR: begin
                    duration <= duration_stage;
                end

                // ?? Fee (NOW PIPELINED) ????????????????????????????????
                OP_CALC_FEE: begin
                    if (fee_mult_stage == 0)
                        fee <= 16'd1;
                    else if (fee_mult_stage > 32'd65535)
                        fee <= 16'hFFFF;
                    else
                        fee <= fee_mult_stage[15:0];
                end

                // ?? Full check ?????????????????????????????????????????
                OP_CHECK_FULL: begin
                    result_flag <= (occ_count_r == NUM_SLOTS);
                end

                // ?? Empty check ????????????????????????????????????????
                OP_CHECK_EMPTY: begin
                    result_flag <= (occ_count_r == 0);
                end

                // ?? Find free slot ?????????????????????????????????????
                OP_FIND_FREE: begin
                    free_slot       <= free_idx_r;
                    free_slot_valid <= found_r;
                    result_flag     <= found_r;
                end

                default: ;

            endcase
        end
    end

endmodule