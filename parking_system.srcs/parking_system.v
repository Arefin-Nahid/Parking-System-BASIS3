`timescale 1ns/1ps
// =============================================================================
// Module  : parking_system  (TOP MODULE)
// Project : Smart Parking System v2 - Basys3 (Artix-7 XC7A35T-1CPG236C)
// Purpose : Top-level integration connecting all submodules.
//
// ?????????????????????????????????????????????????????????????????????????????
//  SUBMODULE HIERARCHY
// ?????????????????????????????????????????????????????????????????????????????
//   parking_system
//   ??? debounce        ? 3   (entry, exit, pay)
//   ??? time_tracker          (global tick + per-car entry time)
//   ??? parking_alu           (duration, fee, slot-finder)
//   ??? parking_memory        (car status + slot map)
//   ??? control_unit          (FSM - transaction coordinator)
//   ??? seg7_display          (4-digit multiplexed display)
//
// ?????????????????????????????????????????????????????????????????????????????
//  BASYS3 I/O MAPPING
// ?????????????????????????????????????????????????????????????????????????????
//  Signal          Pin     Description
//  ??????????????? ??????? ?????????????????????????????????????????????????
//  clk             W5      100 MHz system oscillator
//  rst             U18     BTNC (centre) - synchronous reset
//  btn_entry_raw   T18     BTNU - car entry request
//  btn_exit_raw    W19     BTNL - car exit request
//  btn_pay_raw     T17     BTNR - payment confirmation
//  sw[3:0]         V17-W17 SW3-SW0 - 4-bit car ID
//  led[7:0]        U16-V14 LD7-LD0 - slot occupancy (1=occupied)
//  seg[6:0]        W7-U7   7-segment cathodes (active-low)
//  an[3:0]         U2-W4   digit anodes (active-low)
//
// ?????????????????????????????????????????????????????????????????????????????
//  DISPLAY MODE SUMMARY
// ?????????????????????????????????????????????????????????????????????????????
//  Mode  FSM State(s)                  Display
//  ????  ????????????????????????????? ????????????????????????????????????????
//  00    IDLE, ERROR, FREE_SLOT        " Fr N" - free slot count (0-8)
//  01    READ_ID?ASSIGN, EXIT_REQUEST  "CA  X" - car ID being processed
//  10    CALC_DURATION                 "NNNN"  - duration in seconds (decimal)
//  11    CALC_FEE, WAIT_PAYMENT        "NNNN"  - fee amount (decimal)
//
// ?????????????????????????????????????????????????????????????????????????????
//  PARAMETERS
// ?????????????????????????????????????????????????????????????????????????????
//  NUM_SLOTS - 8 parking slots (matches 8 LEDs on Basys3)
//  NUM_CARS  - 16 car IDs (4-bit SW input)
//  CLK_FREQ  - 100 MHz (Basys3 oscillator)
//  TICK_DIV  - 100 000 000 ? 1 Hz time units (1 unit = 1 second)
//  FEE_RATE  - 2 fee units per second
//  EMPTY_ID  - 4'hF reserved; not assignable as car ID
// =============================================================================

module parking_system #(
    parameter NUM_SLOTS  = 8,
    parameter NUM_CARS   = 16,
    parameter ID_BITS    = 4,
    parameter SLOT_BITS  = 3,
    parameter CLK_FREQ   = 100_000_000,
    parameter TICK_DIV   = 100_000_000,
    parameter FEE_RATE   = 16'd2,
    parameter [ID_BITS-1:0] EMPTY_ID = {ID_BITS{1'b1}}
)(
    input  wire        clk,
    input  wire        rst,
    // Raw (bouncy) button inputs
    input  wire        btn_entry_raw,
    input  wire        btn_exit_raw,
    input  wire        btn_pay_raw,
    // 4-bit car ID switches
    input  wire [3:0]  sw,
    // LED slot occupancy
    output wire [7:0]  led,
    // 7-segment display (active-low)
    output wire [6:0]  seg,
    output wire [3:0]  an
);  // Note: state_out/error_out/error_code removed from top-level I/O.
          //       They remain inside control_unit for simulation/ILA use.

    // ??????????????????????????????????????????????????????????????????????
    //  DEBOUNCE - one instance per button
    // ??????????????????????????????????????????????????????????????????????
    wire entry_pulse, exit_pulse, pay_pulse;
    wire entry_stable, exit_stable, pay_stable;  // unused in top, but valid ports

    debounce #(.DEBOUNCE_BITS(20)) u_deb_entry (
        .clk(clk), .rst(rst),
        .btn_in    (btn_entry_raw),
        .btn_stable(entry_stable),
        .btn_pulse (entry_pulse)
    );

    debounce #(.DEBOUNCE_BITS(20)) u_deb_exit (
        .clk(clk), .rst(rst),
        .btn_in    (btn_exit_raw),
        .btn_stable(exit_stable),
        .btn_pulse (exit_pulse)
    );

    debounce #(.DEBOUNCE_BITS(20)) u_deb_pay (
        .clk(clk), .rst(rst),
        .btn_in    (btn_pay_raw),
        .btn_stable(pay_stable),
        .btn_pulse (pay_pulse)
    );

    // ??????????????????????????????????????????????????????????????????????
    //  INTER-MODULE WIRES
    // ??????????????????????????????????????????????????????????????????????

    // ?? FSM ? time_tracker ????????????????????????????????????????????????
    wire                  cu_timer_rec_en;
    wire [ID_BITS-1:0]    cu_timer_rec_id;
    wire [ID_BITS-1:0]    cu_timer_qry_id;
    wire [31:0]           current_time;
    wire [31:0]           entry_time;
    wire                  overflow_flag;

    // ?? FSM ? ALU ?????????????????????????????????????????????????????????
    wire [2:0]            cu_alu_opcode;
    wire [15:0]           alu_duration;
    wire [15:0]           alu_fee;
    wire                  alu_result_flag;
    wire [SLOT_BITS-1:0]  alu_free_slot;
    wire                  alu_free_valid;

    // ?? FSM ? memory ??????????????????????????????????????????????????????
    wire                  cu_mem_wen;
    wire                  cu_mem_assign;
    wire [ID_BITS-1:0]    cu_mem_car_id;
    wire [SLOT_BITS-1:0]  cu_mem_slot_idx;
    wire                  mem_car_inside;
    wire [NUM_SLOTS-1:0]  slot_occupied;

    // ?? FSM ? display ?????????????????????????????????????????????????????
    wire [1:0]            disp_mode;
    wire [15:0]           disp_fee;
    wire [15:0]           disp_duration;
    wire [3:0]            disp_car_id;

    // ?? Free-slot count - driven by registered output of control_unit ???????
    // Eliminates the duplicate combinational adder tree in top-level.
    wire [3:0] free_count;  // driven by control_unit free_count_out

    // ??????????????????????????????????????????????????????????????????????
    //  SUBMODULE INSTANCES
    // ??????????????????????????????????????????????????????????????????????

    // ?? 1. Time Tracker ???????????????????????????????????????????????????
    time_tracker #(
        .CLK_FREQ(CLK_FREQ),
        .TICK_DIV(TICK_DIV),
        .NUM_CARS(NUM_CARS),
        .ID_BITS (ID_BITS)
    ) u_time (
        .clk            (clk),
        .rst            (rst),
        .record_en      (cu_timer_rec_en),
        .record_id      (cu_timer_rec_id),
        .query_id       (cu_timer_qry_id),
        .current_time   (current_time),
        .entry_time_out (entry_time),
        .overflow_flag  (overflow_flag)
    );

    // ?? 2. ALU ????????????????????????????????????????????????????????????
    parking_alu #(
        .NUM_SLOTS(NUM_SLOTS),
        .SLOT_BITS(SLOT_BITS),
        .RATE     (FEE_RATE)
    ) u_alu (
        .clk            (clk),
        .rst            (rst),
        .opcode         (cu_alu_opcode),
        .current_time   (current_time),
        .entry_time     (entry_time),
        .slot_occupied  (slot_occupied),
        .duration       (alu_duration),
        .fee            (alu_fee),
        .result_flag    (alu_result_flag),
        .free_slot      (alu_free_slot),
        .free_slot_valid(alu_free_valid)
    );

    // ?? 3. Parking Memory ?????????????????????????????????????????????????
    parking_memory #(
        .NUM_SLOTS(NUM_SLOTS),
        .NUM_CARS (NUM_CARS),
        .ID_BITS  (ID_BITS),
        .SLOT_BITS(SLOT_BITS)
    ) u_mem (
        .clk          (clk),
        .rst          (rst),
        .write_en     (cu_mem_wen),
        .op_assign    (cu_mem_assign),
        .car_id       (cu_mem_car_id),
        .slot_idx     (cu_mem_slot_idx),
        .car_inside   (mem_car_inside),
        .car_slot_out (/* not needed in top */),
        .slot_car_out (/* not needed in top */),
        .slot_occupied(slot_occupied),
        .slot_taken   (/* guarded internally */)
    );

    // ?? 4. Control Unit (FSM) ?????????????????????????????????????????????
    control_unit #(
        .NUM_SLOTS(NUM_SLOTS),
        .NUM_CARS (NUM_CARS),
        .ID_BITS  (ID_BITS),
        .SLOT_BITS(SLOT_BITS),
        .EMPTY_ID (EMPTY_ID)
    ) u_ctrl (
        .clk              (clk),
        .rst              (rst),
        // Buttons
        .btn_entry        (entry_pulse),
        .btn_exit         (exit_pulse),
        .btn_pay          (pay_pulse),
        .sw_car_id        (sw),
        // Timer (current_time goes directly to ALU; not needed by FSM)
        .timer_record_en  (cu_timer_rec_en),
        .timer_record_id  (cu_timer_rec_id),
        .timer_query_id   (cu_timer_qry_id),
        .entry_time_in    (entry_time),
        .overflow_flag    (overflow_flag),
        // ALU
        .alu_opcode       (cu_alu_opcode),
        .alu_duration     (alu_duration),
        .alu_fee          (alu_fee),
        .alu_result_flag  (alu_result_flag),
        .alu_free_slot    (alu_free_slot),
        .alu_free_valid   (alu_free_valid),
        // Memory
        .mem_write_en     (cu_mem_wen),
        .mem_op_assign    (cu_mem_assign),
        .mem_car_id       (cu_mem_car_id),
        .mem_slot_idx     (cu_mem_slot_idx),
        .mem_car_inside   (mem_car_inside),
        .slot_occupied    (slot_occupied),
        // Display
        .disp_mode        (disp_mode),
        .disp_fee         (disp_fee),
        .disp_duration    (disp_duration),
        .disp_car_id      (disp_car_id),
        // LEDs
        .led_slots        (led),
        // Free count (registered, for display)
        .free_count_out   (free_count),
        // Debug ports: unconnected at top-level; visible inside u_ctrl for ILA/sim
        .state_out        (),
        .error_out        (),
        .error_code       ()
    );

    // ?? 5. 7-Segment Display ??????????????????????????????????????????????
    seg7_display #(
        .CLK_FREQ  (CLK_FREQ),
        .REFRESH_HZ(1_000),
        .NUM_SLOTS (NUM_SLOTS)
    ) u_seg (
        .clk       (clk),
        .rst       (rst),
        .mode      (disp_mode),
        .free_count(free_count),
        .fee       (disp_fee),
        .duration  (disp_duration),
        .car_id    (disp_car_id),
        .an        (an),
        .seg       (seg)
    );

endmodule